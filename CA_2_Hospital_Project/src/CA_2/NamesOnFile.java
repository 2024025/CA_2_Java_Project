package CA_2;

public class NamesOnFile {
    private String[] name;
    private String[] surname;

    public NamesOnFile(String[] name, String[] surname) {
        this.name = name;
        this.surname = surname;
    }

    public String[] getName() {
        return name;
    }

    public String[] getSurname() {
        return surname;
    }  
}
